my_string = 'Полураспада период'  # Задал переменную
print('First character:', my_string[0])  # Вывел первый символ
print('Last character:', my_string[-1])  # Вывел последний символ
print('Substring:', my_string[2:4])  # Вывел с третьего по пятый символ
print('Length:', len(my_string))  # Вывел количество символов
another_string = ': Начало'  # Задал другую переменную
new_string = my_string + another_string  # Соединил две строки в новую
print('Concatenated string:', new_string)  # Вывел новую строку
